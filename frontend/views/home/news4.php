<p style="text-align:left">　　<strong>南开新闻网讯</strong>（通讯员&nbsp;戴建芳）人成熟红细胞结构简单，有一层依附于质膜的骨架，内部填充着血红蛋白，没有细胞核和其他细胞器。成熟红细胞呈现特有的双凹圆饼状形态，具有极强的变形性和稳定性，使得其在平均120天的寿命期间往返血液循环系统上百万次而不破损。此外，红细胞在流体剪切力的作用下会发生&ldquo;坦克履带运动&rdquo;，细胞膜骨架会沿着剪切力的方向进行旋转运动，而在剪切力停止之后，其恢复到初始状态，原本凹区部分仍然是凹区，边缘区的部分也仍然是边缘区，被称为&ldquo;形状记忆&rdquo;。红细胞独特的双凹圆饼形态和形状记忆特性虽然与其骨架结构形式密切相关，但具体成因仍是未解之谜。</p>

<p style="text-align:left">　　近日，南开大学物理科学学院、泰达应用物理研究院潘雷霆教授、许京军教授团队发展了膨胀显微镜（ultrastructure&nbsp;expansion&nbsp;microscopy，U-ExM）结合单分子定位（single-molecule&nbsp;localization&nbsp;microscopy，SMLM）的超分辨成像技术（U-ExSMLM），达到约6&nbsp;nm的分子分辨率水平，同时在近生理条件下对人红细胞骨架结构展开研究，直观揭示出红细胞双凹圆饼骨架分布存在不对称性，为理解红细胞独特的双凹圆饼形态和形状记忆现象提供了有力的分子水平成像解释。相关结果以&ldquo;Molecular&nbsp;Resolution&nbsp;Mapping&nbsp;of&nbsp;Erythrocyte&nbsp;Cytoskeleton&nbsp;by&nbsp;Ultrastructure&nbsp;Expansion&nbsp;Single-Molecule&nbsp;Localization&nbsp;Microscopy&nbsp;(U-ExSMLM)&rdquo;为题发表在Wiley出版集团旗下刊物《<em>Small&nbsp;Methods</em>》上。</p>

<p style="text-align:left">　　ExM原理是将样品包埋在可以吸水膨胀的凝胶中，随着凝胶的逐渐膨胀，包埋在其中的样品被物理胀大，使得原本距离在普通光学显微镜分辨率极限内的蛋白可以被分辨。SMLM将衍射极限内的荧光分子稀疏闪烁后定位成像，是目前分辨率最高的超分辨成像方法，因此U-ExM和SMLM的结合有望将分辨率提升到新的高度。但SMLM必不可少的成像缓冲液会使得已经膨胀的凝胶收缩，导致二者结合存在困难。为了突破这一限制，该团队使用多聚赖氨酸处理玻片基底，固定膨胀的凝胶，达到在二维尺度抵抗收缩的作用，成功实现了4.3倍物理膨胀的&nbsp;ExM与SMLM的结合，将已有的25&nbsp;nm分辨率SMLM成像提升至约6&nbsp;nm的分子分辨率水平。</p>

<p style="text-align:left">　　如何在维持红细胞双凹圆饼形态进行细胞膜骨架的超高分辨成像一直以来都是一个挑战。U-ExM由于凝胶包埋的支撑，可在维持红细胞双凹圆饼形态情况下实现抗体免疫标记。团队摸索出可维持红细胞双凹圆饼形态的弱黏附条件，结合U-ExSMLM对其进行成像，发现红细胞凹区的血影蛋白骨架的长度大于边缘区，且边缘区的血影蛋白骨架密度大于凹区，直观揭示出双凹圆饼态的红细胞骨架存在不对称性分布，为理解红细胞独特的双凹圆饼形态和形状记忆现象提供了有力的分子水平成像解释，也为研究近细胞质膜亚细胞结构与功能提供一种新的超分辨成像方法。</p>

<p style="text-align:left">　　红细胞独特的双凹圆盘形态自发现以来吸引了大量理论和实验工作者对其形成机制进行研究，但至今仍未有定论。已故美国科学院院士Hoffman教授将低渗诱导的球形红细胞血影离心至玻片上，回调渗透压后发现红细胞恢复成双凹时大部分平躺在玻片上，而非直立或随机分布，认为红细胞存在平行于离心场的&ldquo;密度带&rdquo;使其在离心过程中方向一致，即在宏观水平揭示红细胞边缘区骨架密度高于凹区（<em>PNAS</em>,&nbsp;2016;&nbsp;<em>PNAS</em>,&nbsp;2018），并在文章中指出超分辨成像有望在微观水平给出证明。本文U-ExSMLM成像结果在分子水平直观地表明红细边缘区血影蛋白骨架密度大于凹区，验证了Hoffman院士的猜想。</p>

<p style="text-align:left">　　南开大学物理科学学院潘雷霆教授、许京军教授团队长期致力于单分子定位超分辨成像发展及应用方面研究工作，并取得了一系列进展，如基于单分子定位超分辨成像，澄清了近40年来关于红细胞血影蛋白生理状态长度的疑问（<em>Cell&nbsp;Reports</em>,&nbsp;2018），揭示了低渗刺激下细胞中间纤维骨架纳米尺度快速可逆的解聚组装特性（<em>Advanced&nbsp;Science</em>,&nbsp;2019），发现了巨噬细胞伪足小体三维形貌呈&ldquo;雪人&rdquo;形状而非传统认为的&ldquo;圆屋顶&rdquo;形状（<em>iScience</em>,&nbsp;2022）等。</p>

<p style="text-align:left">　　本文的共同第一作者是博士生侯梦迪与博士后邢福临，通讯作者为潘雷霆教授，南开大学为第一作者单位。该工作得到了国家重点研发计划、国家自然科学基金、中国博士后科学基金、天津市自然科学基金、南开大学细胞应答交叉科学中心等资助。</p>

