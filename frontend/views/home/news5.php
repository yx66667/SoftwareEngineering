<p style="text-align:left">　　南开大学博物馆正式成立于2014年5月，收藏各类质地的藏品1万余件，涵盖陶瓷器、青铜器、钱币、玺印、少数民族文物等十几个门类。现有两个常设展厅，分别是位于范孙楼的文物展厅和位于东方艺术大楼的书画展厅，总展示面积近1000㎡。津南校区思源堂作为文物馆新馆舍，目前正在进行展厅装饰布展工作，预计将于2023年面向公众开放。</p>

