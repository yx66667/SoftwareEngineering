<p style="text-align: center;"><span style="font-family:楷体;">电化学转化石灰石（CaCO<sub>3</sub>）制消石灰（Ca(OH)<sub>2</sub>）及有价值碳质产物示意图。</span></p>

<p style="text-align:left">　　2021年，南开大学与安徽芜湖市人民政府、海螺集团开启战略合作，罗景山教授作为项目负责人与海螺集团全资子公司三碳（安徽）科技研究院有限公司共建南开大学-海螺集团&ldquo;二氧化碳资源化综合利用&rdquo;联合实验室，共同探索二氧化碳资源化利用方式和水泥行业碳减排途径，助力水泥行业绿色低碳发展，用科技支撑我国实现&ldquo;双碳&rdquo;目标。</p>

<p style="text-align:left">　　针对石灰石（CaCO<sub>3</sub>）热分解生成生石灰（CaO）过程中排放二氧化碳（CO<sub>2</sub>）的难题，罗景山课题组基于以往报道的电化学转化CaCO<sub>3</sub>制备Ca(OH)<sub>2</sub>研究，并结合课题组在电化学水分解和二氧化碳还原反应方面的研究基础，提出了一种基于电化学系统的石灰石转化生产消石灰和有价值碳质产物的方法。有别于石灰石高温热解方法，该方法不排放二氧化碳，而是将石灰石中的碳元素直接转化成有价值的碳质产物，可以用作燃料和化学品，为水泥行业碳减排提供了新的思路。</p>

<p style="text-align:left">　　首先，本工作基于中性水分解反应体系对CaCO<sub>3</sub>进行处理转化。此过程利用中性水分解反应中析氧反应过程产生的氢离子（H<sup>+</sup>）与CaCO<sub>3</sub>反应生成钙离子（Ca<sup>2+</sup>）及二氧化碳，Ca<sup>2+</sup>与体系中生成的氢氧根（OH<sup>-</sup>）结合形成Ca(OH)<sub>2</sub>，可直接用于水泥生产。其次，通过切换施加电压，将体系中生成的二氧化碳原位转化成有价值的碳质产物，如一氧化碳、甲烷、烯烃等，反应产物可以通过调换催化剂实现调控。</p>

