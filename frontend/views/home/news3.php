    <td style="padding-top:25px; padding-left:20px; padding-bottom:20px;" id="txt"><p style="text-align:left">　　<strong>南开新闻网讯</strong>（通讯员&nbsp;谢起贤）近日，南开大学电子信息与光学工程学院光电子薄膜器件与技术研究所罗景山教授课题组，针对水泥生产过程中大量碳排放问题，结合课题组在电化学水分解和二氧化碳还原反应方面的研究基础，提出了一种基于电化学的石灰石（CaCO<sub>3</sub>）转化生产消石灰（Ca(OH)<sub>2</sub>）和有价值碳质产物的方法。有别于传统水泥生产制备工艺石灰石高温热解释放二氧化碳同时得到生石灰（CaO）的方法，该方法不排放二氧化碳，而是将石灰石中的碳元素转化成有价值的碳质产物，可以用作燃料和化学品生产，未来有望用于水泥行业脱碳，助力我国实现&ldquo;双碳&rdquo;目标。该研究以&ldquo;Electrochemical&nbsp;transformation&nbsp;of&nbsp;limestone&nbsp;into&nbsp;calcium&nbsp;hydroxide&nbsp;and&nbsp;valuable&nbsp;carbonaceous&nbsp;products&nbsp;for&nbsp;decarbonizing&nbsp;cement&nbsp;production&rdquo;为题发表在国际学术期刊iScience上。</p>

<p style="text-align:left">　　在众多工业生产过程中，建筑材料水泥的生产是最大的二氧化碳排放源之一。2020年水泥行业碳排放占我国碳排放总量的13.5%，水泥行业绿色低碳发展对我国实现&ldquo;双碳&rdquo;目标至关重要。统计报告显示，生产1吨水泥约排放0.6吨二氧化碳，其中约60%排放来自于石灰石的热分解，其余约40%排放来自于加热过程中化石燃料利用及相关设备的电力消耗。化石燃料利用和电力消耗产生的二氧化碳排放可以通过使用可再生燃料和可再生能源来减排，但消除石灰石热分解生成生石灰过程中排放的二氧化碳仍然面临巨大挑战。</p>

