<p style="text-align:left">　　<span style="font-family:楷体;">(A)&nbsp;电解池体系中石灰石（CaCO<sub>3</sub>）电化学转化为消石灰（Ca(OH)<sub>2</sub>）和碳质产物过程的示意图；(B)&nbsp;CaCO<sub>3</sub>、(C)&nbsp;Ca(OH)<sub>2</sub>扫描电子显微镜图像及(D)&nbsp;X射线衍射谱图；(E)&nbsp;不同金属电极在CaCO<sub>3</sub>中性电解液体系中生成有价值碳质产物的法拉第效率。</span></p>

<p style="text-align:left">　　该论文相关技术已由南开大学和海螺集团联合申请国家发明专利。此项技术提出了基于电化学法进行水泥生产来实现水泥行业脱碳的新概念，目前仍处于实验室科学研究阶段，未来实际应用还需进一步研究，罗景山教授团队正在对反应体系和反应器件进行优化设计，以期实现工业化应用的目标。</p>

<p style="text-align:left">　　该论文第一作者为谢起贤博士，罗景山教授为通讯作者。该研究工作得到了海螺集团、国家重点研发计划、国家自然科学基金优秀青年基金、天津市杰出青年基金以及物质绿色创造与制造海河实验室资助。</p>

